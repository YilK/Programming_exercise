#include <iostream>
using namespace std;
int main()
{
	double n;
	cin>>n;
	double res;
	if(n<=100000)
		res=n*0.1;
	else if(n<=200000)
		res=10000+(n-100000)*0.075;
	else if(n<=400000)
		res=10000+100000*0.075+(n-200000)*0.05;
	else if(n<=600000)
		res=10000+100000*0.075+200000*0.05+(n-400000)*0.03;
	else if(n<=1000000)
		res=10000+100000*0.075+200000*0.05+200000*0.03+(n-600000)*0.015;
	else
		res=10000+100000*0.075+200000*0.05+200000*0.03+(400000)*0.015+(n-1000000)*0.01;
	printf("%.2lf\n",res);
	return 0;	
}
